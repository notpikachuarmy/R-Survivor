function loadAssets(callback) {
  const images = [];

  function collect(obj) {
    for (const key in obj) {
      if (obj[key] instanceof Image) {
        images.push(obj[key]);
      } else {
        collect(obj[key]);
      }
    }
  }

  collect(Assets);

  let loaded = 0;

  if (images.length === 0) {
    callback();
    return;
  }

  images.forEach((img) => {
    if (img.complete) {
      loaded++;

      if (loaded === images.length) {
        callback();
      }
    } else {
      img.onload = () => {
        loaded++;

        if (loaded === images.length) {
          callback();
        }
      };

      img.onerror = () => {
        console.warn("No se pudo cargar:", img.src);
        loaded++;

        if (loaded === images.length) {
          callback();
        }
      };
    }
  });
}
